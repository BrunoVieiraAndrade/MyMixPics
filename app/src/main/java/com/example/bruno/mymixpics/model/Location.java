public class Location {

    private Double latitude;
    private String name;
    private Double longitude;
    private String id;

    public Double getLatitude() {
        return latitude;
    }

    public String getName() {
        return name;
    }

    public Double getLongitude() {
        return longitude;
    }

    public String getId() {
        return id;
    }

}
